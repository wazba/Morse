Morse = {"•-": "a", "-•••": "b", "-•-•": "c", "-••": "d", "•": "e",
         "••-•": "f", "--•":  "g", "••••": "h", "••": "i", "•---": "j",
         "-•-": "k", "•-••": "l", "--": "m", "-•": "n", "---": "o",
         "•--•": "p", "--•-": "q", "•-•": "r", "•••": "s", "-": "t",
         "••-": "u", "•••-": "v", "•--": "w", "-••-": "x", "-•--": "y",
         "--••": "z"}
Morse1 = ["•-", "-•••", "-•-•", "-••", "•",
          "••-•", "--•", "••••", "••", "•---",
          "-•-", "•-••", "--", "-•", "---",
          "•--•", "--•-", "•-•", "•••", "-",
          "••-", "•••-", "•--", "-••-", "-•--",
          "--••"]


def kk(text):
    true = 0

    for i in range(0, 26):
        if text == Morse1[i]:
            true += 1
            return Morse[text]
    if true == 0:
        return "no morse"


print(kk(input("Write some morse:  ")))
